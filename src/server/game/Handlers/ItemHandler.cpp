bool WorldPackets::Item::TransmogrifyItems::compareBonusList(std::vector<uint32, std::allocator<char32_t>> const &bonusList, ItemInstance const &itemInst)
{
    if (!itemInst.ItemBonus.is_initialized() && bonusList.size() == 0)
        return true;


    if (itemInst.ItemBonus.get().BonusListIDs.size() != bonusList.size())
        return false;

    for (int32 bonusListID1 : itemInst.ItemBonus.get().BonusListIDs)
    {
        bool matchFound = false;
        for (uint32 bonusListID2 : bonusList)
        {
            if (bonusListID1 == static_cast<int32>(bonusListID2))
            {
                matchFound = true;
                break;
            }
        }
        if (!matchFound)
            return false;
    }

    return true;
}

void WorldSession::HandleTransmogrifyItems(WorldPackets::Item::TransmogrifyItems& packet)
{
    Player* player = GetPlayer();

    if (packet.ItemCount >= EQUIPMENT_SLOT_END)
    {
        TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) sent a wrong count (%u) when transmogrifying items.", player->GetGUID().ToString().c_str(), player->GetName().c_str(), packet.ItemCount);
        return;
    }

    if (!player->GetNPCIfCanInteractWith(packet.NpcGUID, UNIT_NPC_FLAG_TRANSMOGRIFIER))
    {
        TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - %s not found or player can't interact with it.", packet.NpcGUID.ToString().c_str());
        return;
    }

    int64 cost = 0;
    std::vector<Item*> transmogDestItems; // Items to be transmogrified
    std::vector<Item*> transmogSrcItems;
    std::vector<bool> transmogVoid; // Used to deallocate created items

    for (auto &info : packet.Info)
    {
            // slot of the item to transmogrify
            if (info.Slot >= EQUIPMENT_SLOT_END)
            {
                TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify %s with a wrong slot (%u) when transmogrifying items.", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.SrcItemGUID.ToString().c_str(), info.Slot);
                return;
            }

            // transmogrified item
            Item* dest = player->GetItemByPos(INVENTORY_SLOT_BAG_0, info.Slot);
            if (!dest)
            {
                TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify an invalid item in a valid slot (slot: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.Slot);
                return;
            }

            if (!dest->CanTransmogrify())
            {
                TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify an invalid item (item: %u)", player->GetGUID().ToString().c_str(), player->GetName().c_str(), dest->GetEntry());
                return;
            }

            cost += dest->GetSpecialPrice();

            Item* src = NULL;
            if (info.Item.ItemID)
            {
                ItemTemplate const* proto = sObjectMgr->GetItemTemplate(info.Item.ItemID);
                if (!proto)
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify to an invalid item (entry: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.Item.ItemID);
                    return;
                }

                if (!info.SrcItemGUID.IsEmpty())
                    src = player->GetItemByGuid(info.SrcItemGUID);
                else if (!info.SrcVoidItemGUID.IsEmpty())
                {
                    uint8 slot = 0;
                    VoidStorageItem* voidItem = player->GetVoidStorageItem(info.SrcVoidItemGUID.GetCounter(), slot);

                    if (!voidItem)
                    {
                        TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify with an item not present in void storage (entry: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.Item.ItemID);
                        return;
                    }

                    src = Item::CreateItem(info.Item.ItemID, 1);
                    if (voidItem->ItemRandomPropertyId)
                        src->SetItemRandomProperties(voidItem->ItemRandomPropertyId);
                    src->SetUInt32Value(ITEM_FIELD_PROPERTY_SEED, voidItem->ItemSuffixFactor);
                    src->SetGuidValue(ITEM_FIELD_CREATOR, voidItem->CreatorGuid);
                    src->SetModifier(ITEM_MODIFIER_UPGRADE_ID, voidItem->ItemUpgradeId);
                    for (int32 bonusListID : voidItem->BonusListIDs)
                        src->AddBonuses(bonusListID);

                    transmogVoid.push_back(true);
                }

                if (!src)
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify with an invalid item (entry: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), src->GetEntry());
                    return;
                }

                if (src->GetEntry() != info.Item.ItemID)
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify with an invalid item entry (entry: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.Item.ItemID);
                    return;
                }

                if (!packet.compareBonusList(src->GetDynamicValues(ITEM_DYNAMIC_FIELD_BONUSLIST_IDS), info.Item))
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify with an item not with mismatching BonusListIDs (entry: %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), info.Item.ItemID);
                    return;
                }

                if (!src->CanTransmogrify())
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) tried to transmogrify with an invalid item (entry: %u)", player->GetGUID().ToString().c_str(), player->GetName().c_str(), src->GetEntry());
                    return;
                }

                if (!Item::CanTransmogrifyItemWithItem(src, dest))
                {
                    TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) failed CanTransmogrifyItemWithItem (%u with %u).", player->GetGUID().ToString().c_str(), player->GetName().c_str(), src->GetEntry(), dest->GetEntry());
                    return;
                }
            }

            transmogDestItems.push_back(dest);
            transmogSrcItems.push_back(src);

            if (transmogVoid.size() < transmogDestItems.size())
                transmogVoid.push_back(false); // If no true value was added assume false

    }

    if (transmogDestItems.size() != transmogSrcItems.size() || transmogSrcItems.size() != static_cast<uint32>(packet.ItemCount))
    {
        TC_LOG_DEBUG("network", "WORLD: HandleTransmogrifyItems - Player (%s, name: %s) has an invalid number of transmogrification items.", player->GetGUID().ToString().c_str(), player->GetName().c_str());
        return;
    }

    if (cost)
    {
        if (!player->HasEnoughMoney(cost))
            return;
        player->ModifyMoney(-cost);
    }

    for (uint8 i = 0; i < packet.ItemCount; i++)
    {
        // Remove transmogrification
        if (!transmogSrcItems[i])
        {
            transmogDestItems[i]->SetModifier(ITEM_MODIFIER_TRANSMOG_ITEM_ID, 0);
            transmogDestItems[i]->SetModifier(ITEM_MODIFIER_TRANSMOG_APPEARANCE_MOD, 0);
            player->SetVisibleItemSlot(packet.Info[i].Slot, transmogDestItems[i]);
        }
        else
        {
            transmogDestItems[i]->SetModifier(ITEM_MODIFIER_TRANSMOG_ITEM_ID, transmogSrcItems[i]->GetEntry());
            transmogDestItems[i]->SetModifier(ITEM_MODIFIER_TRANSMOG_APPEARANCE_MOD, transmogSrcItems[i]->GetAppearanceModId());
            player->SetVisibleItemSlot(packet.Info[i].Slot, transmogDestItems[i]);

            transmogDestItems[i]->UpdatePlayedTime(player);

            transmogDestItems[i]->SetOwnerGUID(player->GetGUID());
            transmogDestItems[i]->SetNotRefundable(player);
            transmogDestItems[i]->ClearSoulboundTradeable(player);

            if (transmogVoid[i])
                delete transmogSrcItems[i];
            else
            {
                if (transmogSrcItems[i]->GetTemplate()->GetBonding() == BIND_WHEN_EQUIPED || transmogSrcItems[i]->GetTemplate()->GetBonding() == BIND_WHEN_USE)
                    transmogSrcItems[i]->SetBinding(true);

                transmogSrcItems[i]->SetOwnerGUID(player->GetGUID());
                transmogSrcItems[i]->SetNotRefundable(player);
                transmogSrcItems[i]->ClearSoulboundTradeable(player);
            }
        }
        transmogDestItems[i]->SetState(ITEM_CHANGED, player);
    }
}

bool WorldSession::CanUseBank(ObjectGuid bankerGUID) const
{
    // bankerGUID parameter is optional, set to 0 by default.
    if (!bankerGUID)
        bankerGUID = m_currentBankerGUID;

    bool isUsingBankCommand = (bankerGUID == GetPlayer()->GetGUID() && bankerGUID == m_currentBankerGUID);

    if (!isUsingBankCommand)
    {
        Creature* creature = GetPlayer()->GetNPCIfCanInteractWith(bankerGUID, UNIT_NPC_FLAG_BANKER);
        if (!creature)
            return false;
    }

    return true;
}
