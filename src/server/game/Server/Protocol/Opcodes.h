/// Lookup opcode name for human understandable logging (T = OpcodeClient|OpcodeServer)
template<typename T>
inline std::string GetOpcodeNameForLogging(T id)
{
    uint16 opcode = uint16(id);
    std::ostringstream ss;
    ss << '[';
    if (OpcodeHandler const* handler = opcodeTable[T(opcode & 0x7FF)])
        ss << handler->Name;

    ss << " 0x" << std::hex << std::setw(3) << std::setfill('0') << std::uppercase << opcode << std::nouppercase << " (" << std::dec << opcode << ")]";
    return ss.str();
}

